export function getReadingsForDate(date) {
  const year = getLectionaryYear(date);
  const season = getLiturgicalSeason(date);
  const allReadings = READINGS[year];
  const sn = season.name.toLowerCase();
  const wk = (season.week || '').toLowerCase();

  let pool = null;
  if (sn.includes('advent'))        pool = allReadings.advent;
  else if (sn.includes('christmas')) pool = allReadings.christmas;
  else if (sn.includes('epiphany'))  pool = allReadings.epiphany;
  else if (sn.includes('lent'))      pool = allReadings.lent;
  else if (sn.includes('easter'))    pool = allReadings.easter;
  else if (sn.includes('pentecost')) pool = allReadings.pentecost;
  else                               pool = allReadings.ordinaryTime;

  if (!pool) return null;
  const match = pool.find(rd => wk.includes(rd.week.toLowerCase().split(' ').slice(0,2).join(' ').toLowerCase()));
  return match || pool[0];
}
