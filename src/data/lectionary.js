export function getEasterSunday(year) {
  const a = year % 19, b = Math.floor(year / 100), c = year % 100;
  const d = Math.floor(b / 4), e = b % 4, f = Math.floor((b + 8) / 25);
  const g = Math.floor((b - f + 1) / 3);
  const h = (19 * a + b - d - g + 15) % 30;
  const i = Math.floor(c / 4), k = c % 4;
  const l = (32 + 2 * e + 2 * i - h - k) % 7;
  const m = Math.floor((a + 11 * h + 22 * l) / 451);
  const month = Math.floor((h + l - 7 * m + 114) / 31);
  const day = ((h + l - 7 * m + 114) % 31) + 1;
  return new Date(year, month - 1, day);
}

export function getAdventSunday(year) {
  const christmas = new Date(year, 11, 25);
  const dow = christmas.getDay();
  const sundayBefore = new Date(year, 11, 25 - (dow === 0 ? 7 : dow));
  const advent1 = new Date(sundayBefore);
  advent1.setDate(advent1.getDate() - 21);
  return advent1;
}

export function getLectionaryYear(date) {
  const d = new Date(date);
  const churchYear = d.getMonth() >= 11 ? d.getFullYear() + 1 : d.getFullYear();
  const r = churchYear % 3;
  if (r === 0) return 'A';
  if (r === 1) return 'B';
  return 'C';
}

export function getLiturgicalSeason(date) {
  const d = new Date(date);
  const year = d.getFullYear();
  const easter = getEasterSunday(year);
  const advent = getAdventSunday(year);
  const ashWed = new Date(easter); ashWed.setDate(easter.getDate() - 46);
  const pentecost = new Date(easter); pentecost.setDate(easter.getDate() + 49);
  const epiphany = new Date(year, 0, 6);
  const epiphanyDow = epiphany.getDay();
  const baptismSun = new Date(epiphany);
  baptismSun.setDate(epiphany.getDate() + (epiphanyDow === 0 ? 7 : 7 - epiphanyDow));

  if (d >= advent)      return { ...LITURGICAL_SEASONS.ADVENT,    week: getAdventWeek(d, advent) };
  if (d >= pentecost)   return { ...LITURGICAL_SEASONS.PENTECOST, week: getPentecostWeek(d, pentecost) };
  if (d >= easter)      return { ...LITURGICAL_SEASONS.EASTER,    week: getEasterWeek(d, easter) };
  if (d >= ashWed)      return { ...LITURGICAL_SEASONS.LENT,      week: getLentWeek(d, ashWed) };
  if (d >= baptismSun)  return { ...LITURGICAL_SEASONS.EPIPHANY,  week: getEpiphanyWeek(d, baptismSun) };
  if (d >= new Date(year, 0, 1)) return { ...LITURGICAL_SEASONS.CHRISTMAS, week: 'Christmas Season' };
  return { ...LITURGICAL_SEASONS.ORDINARY, week: 'Ordinary Time' };
}

function getAdventWeek(d, a1)    { return 'Advent ' + Math.min(Math.floor((d - a1) / 6048e5) + 1, 4); }
function getLentWeek(d, aw)      { const w = Math.floor((d - aw) / 6048e5); return w < 1 ? 'Ash Wednesday' : 'Lent ' + Math.min(w, 6); }
function getEasterWeek(d, e)     { const w = Math.floor((d - e) / 6048e5); return w === 0 ? 'Easter Sunday' : 'Easter ' + (w + 1); }
function getEpiphanyWeek(d, b)   { return 'Epiphany ' + (Math.floor((d - b) / 6048e5) + 2); }
function getPentecostWeek(d, p)  { const w = Math.floor((d - p) / 6048e5); return w === 0 ? 'Pentecost Sunday' : w === 1 ? 'Trinity Sunday' : 'Pentecost ' + (w + 1); }

